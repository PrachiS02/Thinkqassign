package com.basic1;

public class SwapNum {

	public static void main(String[] args) {
		int num1,num2,temp;
		num1=20;
		num2=30;
		System.out.println("swapping output");
		System.out.println("before swapping number:"+" num1="+num1+" num2="+num2);
		temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("after swapping number :" +" num1=" +num1+ " num2="+num2);

	}

}
