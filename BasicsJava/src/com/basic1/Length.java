package com.basic1;

public class Length {

	public static void main(String[] args) 
	{
		float length,meter,cm,km;
	    length=10;
		meter=length/10;
		cm=length/100;
		km=length/1000;
		System.out.println("length :"+length);
		System.out.println("length in meter :"+meter);
		System.out.println("length in cm :"+cm);
		System.out.println("length in km :"+km);

	}

}
